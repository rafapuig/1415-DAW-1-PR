using System;
using System.Numerics;

namespace ComplexDemo
{
    class Program
    {
        static void MainX()
        {
            double realCoord;
            double imagCoord;
        }

        static double CalculateMagnitude(double realCoord, double imagCoord)
        {
            return Math.Sqrt(realCoord*realCoord+imagCoord*imagCoord);
        }
    }
}
